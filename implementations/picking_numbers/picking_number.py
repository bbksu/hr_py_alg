def pickingNumbers(a):
    set_a = sorted(set(a))
    adj_a = [set_a[i:i+2] for i in range(0,len(set_a)-1)]
    count = 0
    single_count = 0
    for i, x in adj_a:
        p1, p2 =a.count(i), a.count(x)
        if abs(i-x) <=1:
            size = p1 + p2
            if size >= count:
                count = size
        single_count = max(count, single_count, p1, p2)
    if len(set_a) == 1:
        count = a.count(set_a[0])
    count = max(count, single_count)
    return(count)


